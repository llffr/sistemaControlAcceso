CREATE TABLE CARGO (
	IDCARGO INT IDENTITY(1,1) PRIMARY KEY,
	NOMCARGO VARCHAR(100)
);

CREATE TABLE PERSONA (
	IDPER INT IDENTITY(1,1),
	DNI VARCHAR(10) PRIMARY KEY,
	NOMBRE VARCHAR(100),
	APELLIDOS VARCHAR(100),
	TELF VARCHAR(20),
	IDCARGO INT,
	SECCION INT DEFAULT 1,
	SUBSECCION INT DEFAULT 1,
	ESTADO BIT DEFAULT 1,
	clave VARCHAR(100),
	FOREIGN KEY (IDCARGO) REFERENCES CARGO(IDCARGO)
);

CREATE TABLE CONTROLASISTENCIA (
	IDCONTROL INT IDENTITY(1,1) PRIMARY KEY,
	DNI VARCHAR(10),
	DNIBARRA VARCHAR(100),
	H_ING1 DATETIME,
	H_ING2 DATETIME,
	H_SAL1 DATETIME,
	H_SAL2 DATETIME,
	FECHAING DATE DEFAULT GETDATE(),
	FOREIGN KEY (DNI) REFERENCES PERSONA(DNI)
);

