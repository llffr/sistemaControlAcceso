CREATE PROC [dbo].[sp_actualizarPersonal]
@DNI VARCHAR(10),
@NOM VARCHAR(100),
@APE VARCHAR(100),
@TELE VARCHAR(20),
@IDCARGO INT,
@clave varchar(100)
AS
BEGIN
	IF (SELECT COUNT(*) FROM PERSONA WHERE DNI = @DNI) = 0
		BEGIN
			PRINT 'LA PERSONA NO EXISTE'
		END
	ELSE
		BEGIN
			UPDATE PERSONA
			SET NOMBRE = @NOM,
			APELLIDOS = @APE,
			TELF = @TELE,
			IDCARGO = @IDCARGO
			where clave=@clave and DNI=@DNI
			PRINT 'DATOS DE LA PERSONA ACTUALIZADOS CORRECTAMENTE'
		END
	END
	GO

